  var LANGS  = ['en', 'ar', 'he'];
  var LABELS = { en: 'EN', ar: 'عر', he: 'עב' };
  var IS_RTL = { ar: true, he: true };

  function currentLang() {
    var stored = localStorage.getItem('lang');
    return LANGS.indexOf(stored) !== -1 ? stored : 'en';
  }

  function applyLang(code) {
    if (LANGS.indexOf(code) === -1) code = 'en';
    var dict  = T[code];
    var isRtl = !!IS_RTL[code];

    document.documentElement.lang = code;
    document.documentElement.dir  = isRtl ? 'rtl' : 'ltr';
    localStorage.setItem('lang', code);

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var val = dict[key] || T.en[key] || '';
      el.innerHTML = val.replace(/\n/g, '<br>');
    });

    document.querySelectorAll('#langCode').forEach(function (el) {
      el.textContent = LABELS[code];
    });

    document.querySelectorAll('[data-lang]').forEach(function (btn) {
      btn.classList.toggle('lang-option--active', btn.getAttribute('data-lang') === code);
    });
  }

