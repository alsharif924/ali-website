  function initSwitcher(sw) {
    sw.innerHTML = SWITCHER_HTML;

    var btn = sw.querySelector('.lang-btn');
    btn.addEventListener('click', function (e) {
      e.stopPropagation();
      sw.classList.toggle('open');
    });

    sw.querySelectorAll('[data-lang]').forEach(function (opt) {
      opt.addEventListener('click', function () {
        applyLang(opt.getAttribute('data-lang'));
        sw.classList.remove('open');
      });
    });
  }

  document.addEventListener('DOMContentLoaded', function () {
    injectStyles();
    document.querySelectorAll('.lang-switcher').forEach(initSwitcher);
    document.addEventListener('click', function () {
      document.querySelectorAll('.lang-switcher.open').forEach(function (sw) {
        sw.classList.remove('open');
      });
    });
    applyLang(currentLang());
  });
